public class  AssignmentOperators {
	public static void main(String[] args){
		int itemsAvailable = 7;
		System.out.println("Current available items: " + itemsAvailable);
		itemsAvailable += 10;
		System.out.println("Current available items: " + itemsAvailable);
		itemsAvailable -= 9;
		System.out.println("Current available items: " + itemsAvailable);
	}
}
